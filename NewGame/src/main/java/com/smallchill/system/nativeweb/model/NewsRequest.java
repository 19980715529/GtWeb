package com.smallchill.system.nativeweb.model;

import java.util.Date;

import com.smallchill.core.base.model.BaseModel;

//公告配置表
public class NewsRequest extends BaseModel {
	private static final long serialVersionUID = -3615619356227473285L;
	private Integer newsId; //唯一标识
	private String subject; //主标题
	private String body;//公告内容
	private Date deployTime;//发送结束时间
	private Integer interval;//间隔时间
	private Integer startHour;//间隔时间
	private Integer endHour;//间隔时间
	private Integer ptypeid ;
	private Integer type;
	public Integer getNewsId() {
		return newsId;
	}
	public void setNewsId(Integer newsId) {
		this.newsId = newsId;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public String getBody() {
		return body;
	}
	public void setBody(String body) {
		this.body = body;
	}
	public Date getDeployTime() {
		return deployTime;
	}
	public void setDeployTime(Date deployTime) {
		this.deployTime = deployTime;
	}
	public Integer getInterval() {
		return interval;
	}
	public void setInterval(Integer interval) {
		this.interval = interval;
	}
	public Integer getStartHour() {
		return startHour;
	}
	public void setStartHour(Integer startHour) {
		this.startHour = startHour;
	}
	public Integer getEndHour() {
		return endHour;
	}
	public void setEndHour(Integer endHour) {
		this.endHour = endHour;
	}
	public Integer getPtypeid() {
		return ptypeid;
	}
	public void setPtypeid(Integer ptypeid) {
		this.ptypeid = ptypeid;
	}
	public Integer getType() {
		return type;
	}
	public void setType(Integer type) {
		this.type = type;
	}
}
