package com.smallchill.system.treasure.model;

import com.smallchill.core.annotation.BindID;
import com.smallchill.core.annotation.DbName;
import com.smallchill.core.base.model.BaseModel;
import org.beetl.sql.core.annotatoin.Table;

import java.util.Date;

/**
 * @Description 玩家总赢
 * @classNameUserWin
 * @Date 2023/2/16 17:53
 * @Version 1.0
 **/
@DbName(name="gameuserdb")
@Table(name = "AA_Shop_Prop_UserProp")
@BindID(name = "User_Id")
public class UserWin extends BaseModel {
    private Integer user_Id;
    private Integer prop_Id;
    private Long amount;
    private Integer remainTime;
    private Date startTime;
    private Date stopTime;
    private Integer isUse;

    public Integer getUser_Id() {
        return user_Id;
    }

    public void setUser_Id(Integer user_Id) {
        this.user_Id = user_Id;
    }

    public Integer getProp_Id() {
        return prop_Id;
    }

    public void setProp_Id(Integer prop_Id) {
        this.prop_Id = prop_Id;
    }

    public Long getAmount() {
        return amount;
    }

    public void setAmount(Long amount) {
        this.amount = amount;
    }

    public Integer getRemainTime() {
        return remainTime;
    }

    public void setRemainTime(Integer remainTime) {
        this.remainTime = remainTime;
    }

    public Date getStartTime() {
        return startTime;
    }

    public void setStartTime(Date startTime) {
        this.startTime = startTime;
    }

    public Date getStopTime() {
        return stopTime;
    }

    public void setStopTime(Date stopTime) {
        this.stopTime = stopTime;
    }

    public Integer getIsUse() {
        return isUse;
    }

    public void setIsUse(Integer isUse) {
        this.isUse = isUse;
    }
}
