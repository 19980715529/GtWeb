package com.smallchill.game.newmodel.gameuserdb;

import org.beetl.sql.core.annotatoin.Table;

import com.smallchill.core.annotation.DbName;
import com.smallchill.core.base.model.BaseModel;

@DbName(name = "gameuserdb")
@Table(name = "L_PhoneLimit")
@SuppressWarnings("serial")
public class LPhonelimit extends BaseModel {
	private Integer limitrate ;
	private Integer limitvalue ;
	private String account ;
	
	public LPhonelimit() {
	}
	
	public Integer getLimitrate(){
		return  limitrate;
	}
	public void setLimitrate(Integer limitrate ){
		this.limitrate = limitrate;
	}
	
	public Integer getLimitvalue(){
		return  limitvalue;
	}
	public void setLimitvalue(Integer limitvalue ){
		this.limitvalue = limitvalue;
	}
	
	public String getAccount(){
		return  account;
	}
	public void setAccount(String account ){
		this.account = account;
	}
}